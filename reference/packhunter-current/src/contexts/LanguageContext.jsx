const LanguageContext = createContext()

export function LanguageProvider({ children }) {
  const [language, setLanguage] = useState(() => {
    const saved = localStorage.getItem('ptcgp-language')
    if (saved && translations[saved]) return saved

    // Try to detect browser language
    const browserLang = navigator.language.split('-')[0]
    if (translations[browserLang]) return browserLang

    return 'en'
  })

  useEffect(() => {
    localStorage.setItem('ptcgp-language', language)
    document.documentElement.lang = language
  }, [language])

  // Translation function
  const t = (key, fallback = null) => {
    const langTranslations = translations[language] || translations.en
    return langTranslations[key] || translations.en[key] || fallback || key
  }

  // Format number according to locale
  const formatNumber = (num) => {
    return new Intl.NumberFormat(language).format(num)
  }

  // Format date according to locale
  const formatDate = (date, options = {}) => {
    return new Intl.DateTimeFormat(language, options).format(new Date(date))
  }

  // Format relative time
  const formatRelativeTime = (date) => {
    const rtf = new Intl.RelativeTimeFormat(language, { numeric: 'auto' })
    const diff = new Date(date) - new Date()
    const minutes = Math.round(diff / 60000)
    const hours = Math.round(diff / 3600000)
    const days = Math.round(diff / 86400000)

    if (Math.abs(minutes) < 60) return rtf.format(minutes, 'minute')
    if (Math.abs(hours) < 24) return rtf.format(hours, 'hour')
    return rtf.format(days, 'day')
  }

  const value = {
    language,
    setLanguage,
    t,
    formatNumber,
    formatDate,
    formatRelativeTime,
    languages,
    isRTL: false, // Could add RTL support for Arabic, Hebrew, etc.
  }

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider')
  }
  return context
}

export default LanguageContext
